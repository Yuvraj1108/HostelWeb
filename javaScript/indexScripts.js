var messmenu=false;
$("#menu").click(function(){
		if(messmenu==false)
		{
         $("#MessMenu").slideDown();
         messmenu=true;
        }
        else
        {
         $("#MessMenu").slideUp();
         messmenu=false;
        }
   }
)

